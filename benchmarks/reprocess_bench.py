#!/usr/bin/env python3
"""End-to-end benchmark + validation harness for reprocess.

Usage:
    ./reprocess_bench.py <meeting_id> [--label current] [--save]

What it does:
  1. Records file mtimes for journal.jsonl / detected_speakers.json / etc.
  2. POSTs /api/meetings/<id>/reprocess and polls state until complete.
  3. Tails the server log for ``reprocess_phase meeting=<id> phase=... wall_ms=...``
     markers emitted by reprocess.py and assembles per-phase timings.
  4. Runs validate_meeting.py and collects pass/fail + metrics.
  5. Prints a human report AND writes a JSON report to
     benchmarks/results/reprocess_<meeting_id>_<label>_<timestamp>.json
     (if --save is passed).

This makes it safe to compare runs: any change to reprocess.py should
land a fresh report and you can diff the JSON to see if timings improved
and whether any quality metric regressed.
"""

from __future__ import annotations

import argparse
import asyncio
import json
import os
import re
import subprocess
import sys
import time
from datetime import datetime
from pathlib import Path

# Make validate_meeting importable as a sibling module
sys.path.insert(0, str(Path(__file__).parent))
from validate_meeting import validate_meeting

LOG_PATH = Path("/tmp/meeting-scribe.log")
API = "https://localhost:8080"
POLL_INTERVAL_S = 2.0
POLL_TIMEOUT_S = 60 * 30  # 30 min ceiling
PHASE_RE = re.compile(
    r"reprocess_phase meeting=(?P<mid>\S+) phase=(?P<phase>\S+) wall_ms=(?P<wall>\d+)"
)


def _curl(method: str, url: str) -> str:
    """Small curl wrapper — avoids an httpx dependency for the bench script."""
    r = subprocess.run(
        ["curl", "-sk", "-X", method, url],
        capture_output=True,
        text=True,
        check=False,
    )
    return r.stdout


def _get_state(meeting_id: str) -> str:
    body = _curl("GET", f"{API}/api/meetings/{meeting_id}")
    try:
        d = json.loads(body)
    except Exception:
        return "unknown"
    return (d.get("meta") or {}).get("state") or d.get("state") or "unknown"


def _trigger_reprocess(meeting_id: str) -> None:
    _curl("POST", f"{API}/api/meetings/{meeting_id}/reprocess")


def _poll_until_complete(meeting_id: str, deadline: float) -> str:
    while time.monotonic() < deadline:
        state = _get_state(meeting_id)
        if state == "complete":
            return state
        if state in ("error", "failed"):
            return state
        time.sleep(POLL_INTERVAL_S)
    return "timeout"


def _parse_phase_timings(meeting_id: str, since_pos: int) -> dict[str, int]:
    """Read /tmp/meeting-scribe.log from ``since_pos`` and collect
    reprocess_phase markers for this meeting_id."""
    if not LOG_PATH.exists():
        return {}
    timings: dict[str, int] = {}
    with LOG_PATH.open() as f:
        f.seek(since_pos)
        for line in f:
            m = PHASE_RE.search(line)
            if not m:
                continue
            if m.group("mid") != meeting_id:
                continue
            phase = m.group("phase")
            wall = int(m.group("wall"))
            # Multiple markers per phase name are theoretically possible
            # (e.g. retry). Keep the LAST one — it's the one that counted.
            timings[phase] = wall
    return timings


def _pre_run_log_pos() -> int:
    """Mark the log-file offset before triggering reprocess so we can
    read only the lines generated by this run when computing timings."""
    if LOG_PATH.exists():
        return LOG_PATH.stat().st_size
    return 0


async def run_bench(meeting_id: str, label: str, save: bool) -> dict:
    meeting_dir = Path("meetings") / meeting_id
    if not meeting_dir.is_dir():
        raise SystemExit(f"meeting not found: {meeting_id}")

    print(f"\n=== reprocess bench: {meeting_id} (label={label}) ===")
    pcm = meeting_dir / "audio" / "recording.pcm"
    audio_ms = int(os.path.getsize(pcm) / 2 / 16000 * 1000) if pcm.exists() else 0
    print(f"audio: {audio_ms / 60000:.2f}min")

    # Sanity check state
    state = _get_state(meeting_id)
    if state == "reprocessing":
        raise SystemExit(
            "meeting is already in 'reprocessing' state — "
            "another run is in-flight. Wait or stop+restart the server."
        )

    log_pos = _pre_run_log_pos()
    bench_start = time.monotonic()
    print(f"[{_ts()}] triggering reprocess...")
    _trigger_reprocess(meeting_id)

    deadline = bench_start + POLL_TIMEOUT_S
    final_state = _poll_until_complete(meeting_id, deadline)
    bench_wall_ms = int((time.monotonic() - bench_start) * 1000)
    print(f"[{_ts()}] reprocess finished: state={final_state} wall={bench_wall_ms / 1000:.1f}s")

    if final_state != "complete":
        return {
            "meeting_id": meeting_id,
            "label": label,
            "timestamp": datetime.now().isoformat(timespec="seconds"),
            "state": final_state,
            "bench_wall_ms": bench_wall_ms,
            "failed": True,
        }

    # Phase timings from log
    timings = _parse_phase_timings(meeting_id, log_pos)
    print("\n--- phase timings ---")
    for phase, wall in timings.items():
        print(f"  {phase:<24} {wall:>8} ms  ({wall / 1000:>6.1f}s)")
    if "TOTAL" in timings:
        print(f"  {'(bench overhead)':<24} {bench_wall_ms - timings['TOTAL']:>8} ms")

    # Validation
    print("\n--- quality gates ---")
    result = validate_meeting(meeting_dir)
    for c in result.checks:
        mark = "PASS" if c.passed else "FAIL"
        print(f"  [{mark}] {c.name:<28} {c.detail}")
    print(f"\n  OVERALL: {'PASS' if result.all_passed else 'FAIL'}")

    report = {
        "meeting_id": meeting_id,
        "label": label,
        "timestamp": datetime.now().isoformat(timespec="seconds"),
        "state": final_state,
        "audio_ms": audio_ms,
        "bench_wall_ms": bench_wall_ms,
        "phase_timings_ms": timings,
        "quality": result.to_dict(),
    }
    if save:
        out_dir = Path(__file__).parent / "results"
        out_dir.mkdir(exist_ok=True)
        ts = datetime.now().strftime("%Y%m%dT%H%M%S")
        out_path = out_dir / f"reprocess_{meeting_id}_{label}_{ts}.json"
        out_path.write_text(json.dumps(report, indent=2))
        print(f"\nreport saved: {out_path}")

    return report


def _ts() -> str:
    return datetime.now().strftime("%H:%M:%S")


def main() -> int:
    ap = argparse.ArgumentParser(description="Bench + validate a reprocess run.")
    ap.add_argument("meeting", help="meeting_id")
    ap.add_argument("--label", default="current", help="label for the JSON report filename")
    ap.add_argument(
        "--save", action="store_true", help="save JSON report under benchmarks/results/"
    )
    args = ap.parse_args()

    report = asyncio.run(run_bench(args.meeting, args.label, args.save))
    if report.get("failed"):
        return 2
    q = report.get("quality", {})
    return 0 if q.get("all_passed") else 1


if __name__ == "__main__":
    sys.exit(main())
